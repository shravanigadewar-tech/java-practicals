class pract6 {
    public static void main(String[] args) {

        int b = Integer.parseInt("1011", 2);
        int o = Integer.parseInt("17", 8);
        int h = Integer.parseInt("1A", 16);

        System.out.println("Binary to Decimal: " + b);
        System.out.println("Octal to Decimal: " + o);
        System.out.println("Hex to Decimal: " + h);
    }
}
